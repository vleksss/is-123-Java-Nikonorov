CREATE TABLE APP_USER (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR(100) NOT NULL UNIQUE,
    EMAIL VARCHAR(150) NOT NULL UNIQUE,
    PASSWORD VARCHAR(255) NOT NULL,
    ROLE VARCHAR(20) NOT NULL,
    ENABLED BOOLEAN NOT NULL
);

CREATE TABLE AUCTION (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITLE VARCHAR(255) NOT NULL,
    DESCRIPTION VARCHAR(2000) NOT NULL,
    START_PRICE DECIMAL(12,2) NOT NULL,
    START_TIME TIMESTAMP NOT NULL,
    END_TIME TIMESTAMP NOT NULL,
    STATUS VARCHAR(20) NOT NULL,
    OWNER_ID BIGINT NOT NULL,
    CONSTRAINT FK_AUCTION_OWNER FOREIGN KEY (OWNER_ID) REFERENCES APP_USER(ID)
);

CREATE TABLE BID (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    AMOUNT DECIMAL(12,2) NOT NULL,
    BID_TIME TIMESTAMP NOT NULL,
    USER_ID BIGINT NOT NULL,
    AUCTION_ID BIGINT NOT NULL,
    CONSTRAINT FK_BID_USER FOREIGN KEY (USER_ID) REFERENCES APP_USER(ID),
    CONSTRAINT FK_BID_AUCTION FOREIGN KEY (AUCTION_ID) REFERENCES AUCTION(ID)
);

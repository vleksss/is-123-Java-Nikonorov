CREATE TABLE APP_USER (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    USERNAME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(150) NOT NULL,
    PASSWORD VARCHAR(255) NOT NULL,
    ROLE VARCHAR(20) NOT NULL,
    ENABLED BOOLEAN NOT NULL,
    CONSTRAINT PK_APP_USER PRIMARY KEY (ID),
    CONSTRAINT UQ_APP_USER_USERNAME UNIQUE (USERNAME),
    CONSTRAINT UQ_APP_USER_EMAIL UNIQUE (EMAIL),
    CONSTRAINT CHK_APP_USER_ROLE CHECK (ROLE IN ('USER', 'OWNER', 'ADMIN'))
);

CREATE TABLE AUCTION (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    TITLE VARCHAR(255) NOT NULL,
    DESCRIPTION VARCHAR(2000) NOT NULL,
    START_PRICE DECIMAL(15,2) NOT NULL,
    START_TIME TIMESTAMP NOT NULL,
    END_TIME TIMESTAMP NOT NULL,
    STATUS VARCHAR(20) NOT NULL,
    OWNER_ID BIGINT NOT NULL,
    CONSTRAINT PK_AUCTION PRIMARY KEY (ID),
    CONSTRAINT FK_AUCTION_OWNER FOREIGN KEY (OWNER_ID) REFERENCES APP_USER(ID),
    CONSTRAINT CHK_AUCTION_STATUS CHECK (STATUS IN ('DRAFT', 'ACTIVE', 'CLOSED')),
    CONSTRAINT CHK_AUCTION_START_PRICE CHECK (START_PRICE >= 0),
    CONSTRAINT CHK_AUCTION_TIME CHECK (END_TIME > START_TIME)
);

CREATE TABLE BID (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    AMOUNT DECIMAL(15,2) NOT NULL,
    BID_TIME TIMESTAMP NOT NULL,
    USER_ID BIGINT NOT NULL,
    AUCTION_ID BIGINT NOT NULL,
    CONSTRAINT PK_BID PRIMARY KEY (ID),
    CONSTRAINT FK_BID_USER FOREIGN KEY (USER_ID) REFERENCES APP_USER(ID),
    CONSTRAINT FK_BID_AUCTION FOREIGN KEY (AUCTION_ID) REFERENCES AUCTION(ID),
    CONSTRAINT CHK_BID_AMOUNT CHECK (AMOUNT > 0)
);

CREATE INDEX IDX_AUCTION_OWNER ON AUCTION (OWNER_ID);
CREATE INDEX IDX_AUCTION_STATUS ON AUCTION (STATUS);
CREATE INDEX IDX_BID_USER ON BID (USER_ID);
CREATE INDEX IDX_BID_AUCTION ON BID (AUCTION_ID);
CREATE INDEX IDX_BID_AUCTION_AMOUNT ON BID (AUCTION_ID, AMOUNT);
CREATE INDEX IDX_BID_AUCTION_TIME ON BID (AUCTION_ID, BID_TIME);